    // Actualiza los posts del usuario visto
    public void Actualizar() {
        try {
            ResultSet rs = conn.ejecutarConsulta("select contenido from post where nombre_usuario = '" + seguido + "';");
            DefaultListModel dlm = new DefaultListModel();
            while (rs.next()) {
                dlm.addElement(rs.getString("contenido"));
            }
            listPost.setModel(dlm);
            jLabel1.setText(seguido);
        } catch (Exception e) {
        }
    }

    // Comprueba si el usuario que inicia sesi√≥n sigue al que se ve actualmente
    public void ComprobarSigue() {
        try {
            ResultSet rs = conn.ejecutarConsulta("select seguidor from sigue where seguidor = '"
                    + seguidor + "' and seguido = '" + seguido + "';");
            if (rs.next()) {
                jToggleButton1.setText("Dejar de seguir");
                jToggleButton1.setSelected(true);
            } else {
                jToggleButton1.setText("Seguir");
                jToggleButton1.setSelected(true);
            }
        } catch (Exception e) {
        }
    }

